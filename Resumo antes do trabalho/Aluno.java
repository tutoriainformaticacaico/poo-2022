public class Aluno {
    private String nomeDela; 
    private String nomeDele;
    private int idade;
    private double altura;

    /**
     * @return String return the nomeDela
     */
    public String getNomeDela() {
        return nomeDela;
    }

    /**
     * @param nomeDela the nomeDela to set
     */
    public void setNomeDela(String nomeDela) {
        this.nomeDela = nomeDela;
    }

    /**
     * @return String return the nomeDele
     */
    public String getNomeDele() {
        return nomeDele;
    }

    /**
     * @param nomeDele the nomeDele to set
     */
    public void setNomeDele(String nomeDele) {
        this.nomeDele = nomeDele;
    }

    /**
     * @return int return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }

    /**
     * @return double return the altura
     */
    public double getAltura() {
        return altura;
    }

    /**
     * @param altura the altura to set
     */
    public void setAltura(double altura) {
        this.altura = altura;
    }

}
