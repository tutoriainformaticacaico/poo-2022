public class ClasseEditora {
    private String nomeEdit; 
    private String cidade;
    private String cnpj; 
    private String endereço;
    private String telefone;

    public String getNomeEdit() {
        return nomeEdit;
    }
    public void setNomeEdit(String nomeEdit) {
        this.nomeEdit = nomeEdit;
    }
    public String getCidade() {
        return cidade;
    }
    public void setCidade(String cidade) {
        this.cidade = cidade;
    }
    public String getCnpj() {
        return cnpj;
    }
    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }
    public String getEndereço() {
        return endereço;
    }
    public void setEndereço(String endereço) {
        this.endereço = endereço;
    }
    public String getTelefone() {
        return telefone;
    }
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

}
